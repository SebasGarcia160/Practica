# Acertijo de lógica
print("Acertijo: Estoy en el principio de la eternidad, al final del tiempo y el espacio, en el principio de cada final y en el final de cada lugar. ¿Qué soy?")

# Respuesta correcta
respuesta_correcta = "la letra e"

# Captura la respuesta del usuario
respuesta_usuario = input("Escribe tu respuesta: ").strip().lower()

# Comprobación de la respuesta
if respuesta_usuario == respuesta_correcta:
    print("¡Correcto! Has resuelto el acertijo.")
else:
    print("Incorrecto. La respuesta correcta era: 'La letra E'.")